public class whileprogram {
    public static void main(String[] args)
    {
        int n=13;
        int i=2;
        while(n>i)
        {
            if(n%i==0)
            {
            System.out.println(n + " is not a prime");
            
            }
            else
            {
                i++;
            }
        
        }
    }
}
